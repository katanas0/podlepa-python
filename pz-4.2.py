N = int(input("Введите целое число N (>0): "))

# Проверка, является ли N степенью 3
is_power_of_3 = False
if N > 0:
    while N % 3 == 0:  # Пока N делится на 3
        N //= 3
    is_power_of_3 = (N == 1)  # Если после деления осталась 1, то это степень 3

# Вывод результата
print("Является ли число степенью 3:", is_power_of_3)